function saveRollNo() {
  const rollno = document.getElementById("rollno").value.trim();

  if (rollno === "") {
    alert("Please enter your roll number.");
    return false;
  }

  const studentInfo = studentDB[rollno];
  if (!studentInfo) {
    alert("Roll number not found in database!");
    return false;
  }

  // Save to localStorage
  localStorage.setItem("roll_no", rollno);
  localStorage.setItem("studentName", studentInfo.name);
  localStorage.setItem("studentBatch", studentInfo.batch);
  localStorage.setItem("batchNo", studentInfo.batchNo);

  // Redirect to equipment selection page
  window.location.href = "equipment.html";

  return false; // Prevent default form submission
}



